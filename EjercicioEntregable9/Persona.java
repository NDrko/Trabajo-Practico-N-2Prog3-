// Clase Persona
public class Persona {
    String nombre;
    String dni;
    String domicilio;
    int fechaIngreso;

    public Persona(String nombre, String dni, String domicilio, int fechaIngreso) {
        this.nombre = nombre;
        this.dni = dni;
        this.domicilio = domicilio;
        this.fechaIngreso = fechaIngreso;
    }
}
